# Ввести три числа, найти их сумму, произведение и среднее арифметическое.
a =  int(input("введите 1 число "))
b =  int(input("введите 2 число "))
c =  int(input("введите 3 число "))
d = a+b+c
e = a*b*c
f = (a+b+c)/3
print("Сумма этих трех чисел равна", d)
print("Произведение этих трех чисел равно", e)
print("Среднее арифметическое этих трех чисел равно", f)
